package org.getalp.blexisma.impl.semdico;

public interface PersistentlyIteratingSemanticNetwork {
	
	/**
	 * Save the iteration state into the appropriate file.
	 */
	public void saveIterationState();
	
	/**
	 * Return the next ID in the infinite iteration
	 * @return
	 */
	public String next();
	
	/**
	 * return the ID returned by the last call to next.
	 * @return
	 */
	public String lastId();
	
}
